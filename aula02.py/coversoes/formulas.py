def celsius_para_fahrenheit(temperatura_celsius):
    temperatura_fahrenheit= (1.8*temperatura_celsius)+32
    return temperatura_fahrenheit

def libras_para_kg(peso_kg):
    peso_libras= 0.45359237*peso_kg
    return peso_libras

def calcula_posicao(t,s0,v):
    s= s0 + v* t
    return s
def calcula_area_do_triangulo(b,h):
    area= (b*h)/2
    return area